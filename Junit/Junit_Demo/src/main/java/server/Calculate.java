package server;


public class Calculate {
	/*
	 * 有加减乘除四个方法
	 */

	public int add(int num1, int num2) {
		// 一个简单的加法函数
		return num1 + num2;
	}

	public int minus(int num1, int num2) {
		// 一个简单的加法函数
		return num1 - num2;
	}

	public int devide(int num1, int num2) {
		// 一个简单的加法函数
		return num1 / num2;
	}

	public int mul(int num1, int num2) {
		// 一个简单的加法函数
		return num1 * num2;
	}

}
